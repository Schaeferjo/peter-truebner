          Pädagogik auf ihre soziale und gesellschaftliche Bedingtheit aus.
          Durch die Trennung von Sozialpädagogik und Pädagogik seit den 1930er
          Jahren wurde die bildende Funktion der Sozialpädagogik genauso
          vergessen wie die erziehende Funktion, die jede wirkliche Pädagogik
          auszeichnet. Im Rückgriff auf die Geschichte der Pädagogik wird
          deutlich, dass die postmoderne Krise der Pädagogik doppelt selbst
          verschuldet ist: durch die Trennung von Bildung und Erziehung sowie
          durch die Abspaltung einer kritischen Auseinandersetzung der Pädagogik
          mit der sie bedingenden Gesellschaft. Eine Fusion von Pädagogik und
          Sozialer Arbeit bietet Chancen an für beide Professionen.
